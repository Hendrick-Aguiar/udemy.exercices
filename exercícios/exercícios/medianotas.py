#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

notas1 = float(input('insira a nota do 1º Bimestre'))
notas2 = float(input('insira a nota do 2º Bimestre'))
notas3 = float(input('insira a nota do 3º Bimestre'))
notas4 = float(input('insira a nota do 4º Bimestre'))


total = (notas1 + notas2 + notas3 + notas4) / 4




print(f' Sua media final foi de: {total}')
